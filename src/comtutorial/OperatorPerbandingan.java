package comtutorial;

public class OperatorPerbandingan {

    public static void main(String[] args){

        int a = 30;
        int b = 10;
        boolean hasil;

        hasil = a > b;
        System.out.println(hasil);

        hasil = a < b;
        System.out.println(hasil);

        hasil = a == b;
        System.out.println(hasil);

        hasil = a != b;
        System.out.println(hasil);

        hasil = a >= b;
        System.out.println(hasil);

        hasil = a <= b;
        System.out.println(hasil);



    }
}
